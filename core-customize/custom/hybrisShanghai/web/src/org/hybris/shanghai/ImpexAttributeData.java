package org.hybris.shanghai;

/**
 * @author taojiang
 *
 */
public class ImpexAttributeData
{
	private String name;
	private String attributepk;
	private String attributetype;

	public ImpexAttributeData()
	{
		// default constructor
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @param name
	 *           the name to set
	 */
	public void setName(final String name)
	{
		this.name = name;
	}

	/**
	 * @return the attributepk
	 */
	public String getAttributepk()
	{
		return attributepk;
	}

	/**
	 * @param attributepk
	 *           the attributepk to set
	 */
	public void setAttributepk(final String attributepk)
	{
		this.attributepk = attributepk;
	}

	/**
	 * @return the attributetype
	 */
	public String getAttributetype()
	{
		return attributetype;
	}

	/**
	 * @param attributetype
	 *           the attributetype to set
	 */
	public void setAttributetype(final String attributetype)
	{
		this.attributetype = attributetype;
	}

	/**
	 * @return the qualifier
	 */

}
